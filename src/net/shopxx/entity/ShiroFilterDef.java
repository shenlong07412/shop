package net.shopxx.entity;

/**
 * @author czllfy
 */
public class ShiroFilterDef extends BaseEntity {

    /**
     * 
     */
    private static final long serialVersionUID = -6508405955176145911L;
    private String            name;                                    // url名称/描述
    private String            url;                                     // 地址
    private String            roles;                                   // 所需要的角色，可省略
    private String            permissions;                             // 所需要的权限，可省略

    /**
     * @return the name
     */
    public String getName() {
        return name;
    }

    /**
     * @param name the name to set
     */
    public void setName(String name) {
        this.name = name;
    }

    /**
     * @return the url
     */
    public String getUrl() {
        return url;
    }

    /**
     * @param url the url to set
     */
    public void setUrl(String url) {
        this.url = url;
    }

    /**
     * @return the roles
     */
    public String getRoles() {
        return roles;
    }

    /**
     * @param roles the roles to set
     */
    public void setRoles(String roles) {
        this.roles = roles;
    }

    /**
     * @return the permissions
     */
    public String getPermissions() {
        return permissions;
    }

    /**
     * @param permissions the permissions to set
     */
    public void setPermissions(String permissions) {
        this.permissions = permissions;
    }

}
